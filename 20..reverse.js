/*
 the reverse () method reverses the order of the elements in an array in place.

 syntax: arrays.reverse(0)

*/

let letters = ["a", "b", "c", "d"];
letters.reverse();
console.log(letters);

let alphabets = ["a", "b", "c", "d", "e", "f", "g", "h"];
alphabets.reverse();
console.log(alphabets);



let numbers = [1, 2, 34, 45, 66, 777];
numbers.reverse();
console.log(numbers);
